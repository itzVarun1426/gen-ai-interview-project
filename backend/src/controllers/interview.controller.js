import {PDFParse} from 'pdf-parse';
import { generateInterviewReport } from '../services/ai.service.js';
import interviewReportModel from '../models/interviewReport.model.js';


async function generateInterviewReportController(req, res) {
    try{
        const resume = new Uint8Array(req.file.buffer);
        const {selfDescription, jobDescription} = req.body;

        // const resumeContent = await pdfParse(resume).then(data => data.text);
        const resumeContent  = new PDFParse(resume);
        await resumeContent.load();
        console.log("this is resume content",resumeContent)

        const interviewReportGeneratedByAI = await generateInterviewReport({
            resume: resumeContent.text,
            selfDescription,
            jobDescription
        });

        if (!interviewReportGeneratedByAI) {
            return res.status(500).json({
                message: "AI generation failed"
            });
        }

        // console.log("interview report generated by AI",interviewReportGeneratedByAI)



        const interviewReport = await interviewReportModel.create({
            jobDescription,
            resume: resumeContent.text,
            selfDescription,
            user: req.decoded.id,
            ...interviewReportGeneratedByAI
        })


        // console.log(interviewReport)

        res.status(200).json({
            message: 'Interview report generated successfully',
            interviewReport 
        });

    }
    catch(err){
        console.error(err);
        res.status(500).json({ error: 'Failed to generate interview report' });


    }
}

export default {generateInterviewReportController};
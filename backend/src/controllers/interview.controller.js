import {PDFParse} from 'pdf-parse';
import { generateInterviewReport } from '../services/ai.service.js';
import interviewReportModel from '../models/interviewReport.model.js';
import { get } from 'mongoose';


async function generateInterviewReportController(req, res) {
    try{
        const resume = new Uint8Array(req.file.buffer);
        const {selfDescription, jobDescription} = req.body;

        // const resumeContent = await pdfParse(resume).then(data => data.text);
        const resumeContent  = new PDFParse(resume);
        await resumeContent.load();
        console.log("this is resume content",resumeContent)

        const interviewReportGeneratedByAI = await generateInterviewReport({
            resume: resumeContent.text,
            selfDescription,
            jobDescription
        });

        if (!interviewReportGeneratedByAI) {
            return res.status(500).json({
                message: "AI generation failed"
            });
        }

        // console.log("interview report generated by AI",interviewReportGeneratedByAI)



        const interviewReport = await interviewReportModel.create({
            jobDescription,
            resume: resumeContent.text,
            selfDescription,
            user: req.decoded.id,
            ...interviewReportGeneratedByAI
        })


        // console.log(interviewReport)

        res.status(200).json({
            message: 'Interview report generated successfully',
            interviewReport 
        });

    }
    catch(err){
        console.error(err);
        res.status(500).json({ error: 'Failed to generate interview report' });


    }
}

async function getInterviewByIdController(req, res){
    try{
        const {interviewId} = req.params;
        if(!interviewId){
            return res.status(400).json({
                message: "interview id is required"
            });
        }
        const interviewReport = await interviewReportModel.findById(interviewId);

        if(!interviewReport){
            return res.status(404).json({
                message: "interview report not found"
            });

        }
        res.status(200).json({
            message: "interview report found",
            interviewReport
        });
    }
    catch(err){
        console.log("error in interview.api.js ",err)
    }
}

async function getAllInterviewReportByUserId(req,res) {
    try{
        const user = req.decoded.id;
        const reports = await interviewReportModel.find({user}).sort({createdAt: -1}).select("-resume -jobDescription -selfDescription -skillsGaps -behavioralQuestions -technicalQuestions -__v -preparationPlan");
        if(!reports){
            return res.status(404).json({
                message: "interview reports not found"
            });
        }
        res.status(200).json({
            message: "interview reports found",
            reports
        });

    }
    catch(err){
        console.log("error in interview.api.js ",err)
    }
}


export default {generateInterviewReportController,getInterviewByIdController,getAllInterviewReportByUserId};